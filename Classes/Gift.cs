
namespace Classes
{
    public class Gift
    {
        public string name;
        public string color;
        public double weight;
        public double volume;

        public Gift() { }

        public Gift(string name, string color, double weight, double volume) 
        {
            this.name = name;
            this.color = color;
            this.weight = weight;
            this.volume = volume;
        }

    }
}
